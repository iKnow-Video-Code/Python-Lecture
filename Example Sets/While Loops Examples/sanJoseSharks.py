# San Jose Sharks Program - Program that outputs the San Jose Sharks's scores in their Ice Hockey game
# The San Jose Sharks have scored 6 points in total and started at 0
# Each point is increasing by 1

sanJoseSharksScore = 0
while sanJoseSharksScore <= 6:
    print(sanJoseSharksScore)
    sanJoseSharksScore += 1